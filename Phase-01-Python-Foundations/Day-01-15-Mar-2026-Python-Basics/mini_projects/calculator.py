# 1️⃣ Simple Calculator

# Goal : create a calculator to perform addition, subtraction, multiplication and division 
# user should  enter two numbers
#program print all operations
# concept used : variables, input, print, type conversion, arithmetic operators


#taking input from the user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Performing arithmetic operations
addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2

# Printing the results
print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)


 